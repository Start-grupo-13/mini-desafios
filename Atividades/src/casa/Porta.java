package casa;

public class Porta {
	public double altura;
	public String cor;




	public Porta() {
	}

	public Porta(String cor) {
		this.cor = cor;
	}

	public Porta(double altura) {
		this.altura = altura;
	}

	public Porta(double altura, String cor) {
		this.altura = altura;
		this.cor = cor;
	}




}
