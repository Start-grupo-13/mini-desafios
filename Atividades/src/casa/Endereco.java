package casa;

public class Endereco {
	public String rua;
	public String numero;
	public String bairro;
	public String cidade;



	public Endereco() {
	}


	public Endereco(String rua, String numero, String bairro, String cidade) {
		this.rua = rua;
		this.numero = numero;
		this.bairro = bairro;
		this.cidade = cidade;
	}



}
